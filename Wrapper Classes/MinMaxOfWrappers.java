
public class MinMaxOfWrappers {

	public static void main(String[] args) {
		System.out.println("Integer Range:");
		System.out.println("min: "+Integer.MIN_VALUE+"\nmax: "+Integer.MAX_VALUE);
		System.out.println("Double Range:");
		System.out.println("min: "+Double.MIN_VALUE+"\nmax: "+Double.MAX_VALUE);
		System.out.println("Long Range:");
		System.out.println("min: "+Long.MIN_VALUE+"\nmax: "+Long.MAX_VALUE);
		System.out.println("Short Range:");
		System.out.println("min: "+Short.MIN_VALUE+"\nmax: "+Short.MAX_VALUE);
		System.out.println("Byte Range:");
		System.out.println("min: "+Byte.MIN_VALUE+"\nmax: "+Byte.MAX_VALUE);
		System.out.println("Float Range:");
		System.out.println("min: "+Float.MIN_VALUE+"\nmax: "+Float.MAX_VALUE);
	}

}
